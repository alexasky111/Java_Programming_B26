package day08_relational_operators;

public class SignType {
    public static void main(String[] args) {

        int num1 = 0;
        boolean isTheNumberPositive = num1>0;
        boolean isTheNumberNegative = num1<0;
        boolean isTheNumber0 = num1==0;

        System.out.println(isTheNumberPositive);
        System.out.println(isTheNumberNegative);
        System.out.println(isTheNumber0);


    }

}
