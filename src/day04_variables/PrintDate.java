package day04_variables;

public class PrintDate {

    public static void main(String[] args) {
        /*
        Task:
        declare and assign 3 variables the month, the day number, and year number
        print the dates in the following format:
        month/day/year
         */
        int month = 06;
        int day = 30;
        int year = 1994;

        System.out.println( month+ "/" + day+ "/"+ year);
        System.out.println(month+ "<"+ day+ "<"+ year);
        System.out.println(month+ "-"+ day+ "-"+ year);
        System.out.println(month+ "\\"+day+"\\"+year);

        month = 9;
        day = 28;
        year = 2020;

        System.out.println(month+"\\"+day+"\\"+year);
        System.out.println("^-^+^-^=<3");



    }
}
